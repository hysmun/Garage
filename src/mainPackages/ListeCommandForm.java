    class MyTableModel extends AbstractTableModel {

        private List<CentraleCommand> contents;
        private final String[] tableHeaders = {"ID", "Libell√©", "Type", "Quantite","Urgence", "Date", "Disponible"};

        public MyTableModel(List<CentraleCommand> contents){
            this.contents = contents;
        } 

        @Override
        public String getColumnName(int columnIndex) {
            return tableHeaders[columnIndex];
        }
 
        @Override
        public int getRowCount(){
            return this.contents.size();
        }

        @Override
        public int getColumnCount(){
            return 7;
        }

        @Override
        public Object getValueAt(int row, int column){
            CentraleCommand myObj = contents.get(row);

            //map column index to property
            /*
                protected String id;
                protected String libelle;
                protected String type;
                protected int quantite;
                protected int urgence;
                protected Calendar dateEnvois;
                protected boolean disponibilite;
            */
            switch(column)
            {
                case 0://id
                    return myObj.getId();
                case 1://libelle
                    return myObj.getLibelle();
                case 2://type
                    return myObj.getType();
                case 3://quantite
                    return myObj.getQuantite();
                case 4://urgent
                    return myObj.getUrgence();
                case 5://date
                    return myObj.getDateEnvois();
                case 6://dispo
                    return myObj.isDisponibilite();
            }
            return null;
        }
    }
